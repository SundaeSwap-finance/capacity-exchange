const scriptBytes = new PlutusV3Script(HexBlob(SCRIPT_HEX));
export const registrationScript = Script.newPlutusV3Script(scriptBytes);
export const registrationPolicyId = PolicyId(registrationScript.hash());
export const beaconAssetName = AssetName('');

export function getNetworkId(network: CardanoNetwork): NetworkId {
  return network === 'mainnet' ? NetworkId.Mainnet : NetworkId.Testnet;
}

export function requireEnv(name: string): string {
  const value = process.env[name];
  if (!value) throw new Error(`Missing required environment variable: ${name}`);
  return value;
}

/** Enterprise script address for the registration validator. */
export function getScriptAddress(networkId: NetworkId): Address {
  return Address.fromBytes(
    HexBlob((networkId === NetworkId.Mainnet ? '71' : '70') + registrationPolicyId),
  );
}

/**
 * Build the registration datum:
 *   Constr 0 [Constr 0 [stakeKeyHash], dustAddressBytes]
 */
export function buildRegistrationDatum(stakeKeyHash: string, dustAddressHex: string): PlutusData {
  const stakeKeyData = PlutusData.newBytes(Uint8Array.from(Buffer.from(stakeKeyHash, 'hex')));
  const stakeCredFields = new PlutusList();
  stakeCredFields.add(stakeKeyData);
  const stakeCredential = PlutusData.newConstrPlutusData(new ConstrPlutusData(0n, stakeCredFields));

  const dustAddrData = PlutusData.newBytes(Uint8Array.from(Buffer.from(dustAddressHex, 'hex')));
  const datumFields = new PlutusList();
  datumFields.add(stakeCredential);
  datumFields.add(dustAddrData);
  return PlutusData.newConstrPlutusData(new ConstrPlutusData(0n, datumFields));
}

/** Build the mint redeemer: Constr 0 [] (register path). */
export function buildMintRedeemer(): PlutusData {
  return PlutusData.newConstrPlutusData(new ConstrPlutusData(0n, new PlutusList()));
}

/** Build the spend redeemer: Constr 0 [] */
export function buildSpendRedeemer(): PlutusData {
  return PlutusData.newConstrPlutusData(new ConstrPlutusData(0n, new PlutusList()));
}

/** Build the burn redeemer: Constr 1 [] (deregister/burn path). */
export function buildBurnRedeemer(): PlutusData {
  return PlutusData.newConstrPlutusData(new ConstrPlutusData(1n, new PlutusList()));
}

/**
 * Extract the c_wallet (stake key hash) from a registration inline datum.
 * Datum shape: Constr 0 [Constr 0 [stakeKeyHash], dustAddressBytes]
 */
export function extractStakeKeyFromDatum(datum: PlutusData): string {
  const outer = datum.asConstrPlutusData();
  if (!outer) throw new Error('Datum is not a ConstrPlutusData');

  const cWalletField = outer.getData().get(0)!.asConstrPlutusData();
  if (!cWalletField) throw new Error('c_wallet field is not a ConstrPlutusData');

  const stakeKeyBytes = cWalletField.getData().get(0)!.asBoundedBytes();
  if (!stakeKeyBytes) throw new Error('Stake key hash is not bytes');

  return Buffer.from(stakeKeyBytes).toString('hex');
}
